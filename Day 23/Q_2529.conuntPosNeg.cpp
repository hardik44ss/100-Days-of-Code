int negCnt(const vector<int>& nums, int s, int e) {
    int ans = -1;
    while (s <= e) {
        int mid = s + (e - s) / 2;
        if (nums[mid] < 0) {
            ans = mid;
            s = mid + 1;
        } else {
            e = mid - 1;
        }
    }
    return ans + 1;
}

int posCnt(const vector<int>& nums, int s, int e) {
    int ans = -1;
    while (s <= e) {
        int mid = s + (e - s) / 2;
        if (nums[mid] > 0) {
            ans = mid;
            e = mid - 1;
        } else {
            s = mid + 1;
        }
    }if(ans == -1) return 0;
    return nums.size() - ans;
}

int maximumCount(vector<int>& nums) {
    int e = nums.size() - 1;
    int p = posCnt(nums, 0, e);
    int n = negCnt(nums, 0, e);
    return max(p, n);
}

